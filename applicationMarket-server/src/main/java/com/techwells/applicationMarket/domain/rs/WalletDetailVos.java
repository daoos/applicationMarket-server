package com.techwells.applicationMarket.domain.rs;

import java.math.BigDecimal;
import java.util.List;

import com.techwells.applicationMarket.domain.WalletDetail;

/**
 * 钱包明细值对象
 * @author 陈加兵
 */
public class WalletDetailVos {
	private String yearMonth;  //年月
	private BigDecimal pay;  //支出 
	private BigDecimal income;  //收入
	private Integer isShow=1;  //是否显示  1 显示日期，收入，支出  0 不显示
	private List<WalletDetail> details;  //明细
	
	
	public Integer getIsShow() {
		return isShow;
	}
	public void setIsShow(Integer isShow) {
		this.isShow = isShow;
	}
	public String getYearMonth() {
		return yearMonth;
	}
	public void setYearMonth(String yearMonth) {
		this.yearMonth = yearMonth;
	}
	public BigDecimal getPay() {
		return pay;
	}
	public void setPay(BigDecimal pay) {
		this.pay = pay;
	}
	public BigDecimal getIncome() {
		return income;
	}
	public void setIncome(BigDecimal income) {
		this.income = income;
	}
	public List<WalletDetail> getDetails() {
		return details;
	}
	public void setDetails(List<WalletDetail> details) {
		this.details = details;
	}
	
	
	
	
}
