package test;

import java.util.Date;


public class User {
	private String userId;
	private Date b;
	
	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Date getB() {
		return b;
	}

	public void setB(Date b) {
		this.b = b;
	}

	@Override
	public String toString() {
		return "User [userId=" + userId + ", b=" + b + "]";
	}
	
}
